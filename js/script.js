let toastTimer = null;

function getTextByLanguage(idText, enText) {
    return currentLanguage === 'en' ? enText : idText;
}

function showToast(message) {
    const toast = document.getElementById('siteToast');

    if (!toast || !message) return;

    clearTimeout(toastTimer);
    toast.textContent = message;
    toast.setAttribute('aria-hidden', 'false');
    toast.classList.add('show');

    toastTimer = setTimeout(() => {
        toast.classList.remove('show');
        toast.setAttribute('aria-hidden', 'true');
    }, 2600);
}

function scrollToTarget(target) {
    if (target === 'body') {
        window.scrollTo({ top: 0, behavior: 'smooth' });
        return;
    }

    const targetElement = document.querySelector(target);
    if (!targetElement) return;

    const navbar = document.getElementById('navbar');
    const offset = navbar ? navbar.offsetHeight : 0;
    const top = targetElement.getBoundingClientRect().top + window.scrollY - offset;

    window.scrollTo({
        top,
        behavior: 'smooth'
    });
}

function translatePage(lang) {
    if (!i18n[lang]) return;

    currentLanguage = lang;

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        if (i18n[lang][key]) {
            element.innerHTML = i18n[lang][key];
        }
    });

    const languageSelect = document.getElementById('langSelect');
    if (languageSelect) {
        languageSelect.value = lang;
    }

    if (activeMentorId && document.getElementById('mentorModal') && !document.getElementById('mentorModal').classList.contains('hidden')) {
        setMentorModalContent(activeMentorId);
    }
}

function setMentorModalContent(id) {
    const mentor = dataMentor[id];
    const modalImg = document.getElementById('modalImg');
    const modalName = document.getElementById('modalName');
    const modalRole = document.getElementById('modalRole');
    const modalDesc = document.getElementById('modalDesc');

    if (!mentor || !modalImg || !modalName || !modalRole || !modalDesc) return false;

    modalImg.src = mentor.foto;
    modalImg.alt = `Foto ${mentor.nama}`;
    modalName.textContent = mentor.nama;
    modalRole.textContent = mentor.jabatan[currentLanguage] || mentor.jabatan.id;
    modalDesc.textContent = mentor.deskripsi[currentLanguage] || mentor.deskripsi.id;

    return true;
}

