let currentLanguage = 'id';

document.addEventListener("DOMContentLoaded", () => {
    
    // 1. NAVBAR SCROLL EFFECT
    const navbar = document.getElementById('navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('py-2', 'shadow-md');
            navbar.classList.remove('py-0');
        } else {
            navbar.classList.remove('py-2', 'shadow-md');
            navbar.classList.add('py-0');
        }
    });

    // SMOOTH SCROLL NAVBAR MENU KLIK (Menggunakan JS Interceptor)
    document.querySelectorAll('.nav-link').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault(); // Mencegah loncatan instan bawaan HTML
            const targetId = this.getAttribute('href').substring(1);
            const targetSection = document.getElementById(targetId);
            
            if (targetSection) {
                // Menghitung tinggi navbar untuk offset agar judul tidak tertutup
                const navHeight = document.getElementById('navbar').offsetHeight;
                const targetPosition = targetSection.getBoundingClientRect().top + window.scrollY - navHeight;
                
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // 2. LOGIKA GANTI BAHASA
    window.gantiBahasa = function(lang) {
        currentLanguage = lang;
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if(i18n[lang] && i18n[lang][key]) {
                el.innerHTML = i18n[lang][key];
            }
        });

        // Update data mentor jika modal sedang terbuka
        const modalName = document.getElementById('modalName').innerText;
        if (!document.getElementById('mentorModal').classList.contains('hidden')) {
            let activeId = null;
            for(let key in dataMentor) {
                if(dataMentor[key].nama === modalName) activeId = key;
            }
            if(activeId) {
                document.getElementById('modalRole').innerText = dataMentor[activeId].jabatan[lang];
                document.getElementById('modalDesc').innerText = dataMentor[activeId].deskripsi[lang];
            }
        }
    }

    // 3. LOGIKA POPUP MODAL MENTOR
    const modal = document.getElementById('mentorModal');
    const modalOverlay = document.getElementById('modalOverlay');
    const contentBox = document.getElementById('modalContentBox');

    window.bukaModal = function(id) {
        document.getElementById('modalImg').src = dataMentor[id].foto;
        document.getElementById('modalName').innerText = dataMentor[id].nama;
        document.getElementById('modalRole').innerText = dataMentor[id].jabatan[currentLanguage];
        document.getElementById('modalDesc').innerText = dataMentor[id].deskripsi[currentLanguage];
        
        modal.classList.remove('hidden');
        modal.classList.add('flex');
        document.body.classList.add('modal-open');

        setTimeout(() => {
            modalOverlay.classList.remove('opacity-0');
            modalOverlay.classList.add('opacity-100');
            contentBox.classList.remove('scale-95', 'opacity-0');
            contentBox.classList.add('scale-100', 'opacity-100');
        }, 10);
    }

    window.tutupModal = function() {
        modalOverlay.classList.remove('opacity-100');
        modalOverlay.classList.add('opacity-0');
        contentBox.classList.remove('scale-100', 'opacity-100');
        contentBox.classList.add('scale-95', 'opacity-0');
        document.body.classList.remove('modal-open');

        setTimeout(() => {
            modal.classList.add('hidden');
            modal.classList.remove('flex');
        }, 300);
    }

    // 4. LOGIKA FAQ ACCORDION
    window.toggleFAQ = function(targetId, btnElement) {
        const content = document.getElementById(targetId);
        const icon = btnElement.querySelector('i');
        
        if (content.classList.contains('hidden')) {
            content.classList.remove('hidden');
            icon.classList.remove('fa-plus');
            icon.classList.add('fa-minus');
            icon.style.transform = 'rotate(180deg)';
        } else {
            content.classList.add('hidden');
            icon.classList.remove('fa-minus');
            icon.classList.add('fa-plus');
            icon.style.transform = 'rotate(0deg)';
        }
    };

    // Aksi tombol ditangani pada lapisan interaksi stabil di bawah.
});

